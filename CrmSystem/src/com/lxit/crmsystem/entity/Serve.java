package com.lxit.crmsystem.entity;

import java.io.Serializable;
/**
 *服务类
 */
public class Serve implements Serializable{

	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;
	
	private int serveId;//服务ID
	private String serveName;//服务名称
	private int serveState;//服务状态
	private int serveType;//服务类型
	private String serveUpdateTime;//最后一次修改的时间
	private String serveCreateTime;//创建时间
	private int serveManageName;//操作人
	private int serveStayhandle;//待处理人
	private int serveClientId;//服务客户
	private String servePhone;//联系电话
	private String serveContent;//服务内容
	private String serveRemark;//备注信息
	public int getServeId() {
		return serveId;
	}
	public void setServeId(int serveId) {
		this.serveId = serveId;
	}
	public String getServeName() {
		return serveName;
	}
	public void setServeName(String serveName) {
		this.serveName = serveName;
	}
	public int getServeState() {
		return serveState;
	}
	public void setServeState(int serveState) {
		this.serveState = serveState;
	}
	public int getServeType() {
		return serveType;
	}
	public void setServeType(int serveType) {
		this.serveType = serveType;
	}
	public String getServeUpdateTime() {
		return serveUpdateTime;
	}
	public void setServeUpdateTime(String serveUpdateTime) {
		this.serveUpdateTime = serveUpdateTime;
	}
	public String getServeCreateTime() {
		return serveCreateTime;
	}
	public void setServeCreateTime(String serveCreateTime) {
		this.serveCreateTime = serveCreateTime;
	}
	public int getServeManageName() {
		return serveManageName;
	}
	public void setServeManageName(int serveManageName) {
		this.serveManageName = serveManageName;
	}
	public int getServeStayhandle() {
		return serveStayhandle;
	}
	public void setServeStayhandle(int serveStayhandle) {
		this.serveStayhandle = serveStayhandle;
	}
	
	public int getServeClientId() {
		return serveClientId;
	}
	public void setServeClientId(int serveClientId) {
		this.serveClientId = serveClientId;
	}
	public String getServePhone() {
		return servePhone;
	}
	public void setServePhone(String servePhone) {
		this.servePhone = servePhone;
	}
	public String getServeContent() {
		return serveContent;
	}
	public void setServeContent(String serveContent) {
		this.serveContent = serveContent;
	}
	public String getServeRemark() {
		return serveRemark;
	}
	public void setServeRemark(String serveRemark) {
		this.serveRemark = serveRemark;
	}
	public Serve() {
	}
	@Override
	public String toString() {
		return "Serve [serveId=" + serveId + ", serveName=" + serveName + ", serveState=" + serveState + ", serveType="
				+ serveType + ", serveUpdateTime=" + serveUpdateTime + ", serveCreateTime=" + serveCreateTime
				+ ", serveManageName=" + serveManageName + ", serveStayhandle=" + serveStayhandle + ", serveClientId="
				+ serveClientId + ", servePhone=" + servePhone + ", serveContent=" + serveContent + ", serveRemark="
				+ serveRemark + "]";
	}
	
	
	
}
