package com.lxit.crmsystem.entity;

/**
 * @author Administrator
 * 机会表
 */
public class Hap {
	private Integer hapId;  //机会id
	private String hapName;  //机会名称
	private Integer hapType;  //机会类型
	private String hapCreateTime;  //创建时间
	private String hapUpdateTime;  //修改时间
	private Integer hapSid;  //录入人
	private Integer hapUpdateSid;  //最后一次操作人
	
	private String hap_conten;//机会内容
	private String hap_remark;//备注信息
	private String hap_code;//机会编码
	private int hap_staffId;//分配给员工id
	
	@Override
	public String toString() {
		return "Hap [hapId=" + hapId + ", hapName=" + hapName + ", hapType=" + hapType + ", hapCreateTime="
				+ hapCreateTime + ", hapUpdateTime=" + hapUpdateTime + ", hapSid=" + hapSid + ", hapUpdateSid="
				+ hapUpdateSid + ", hap_conten=" + hap_conten + ", hap_remark=" + hap_remark + ", hap_code=" + hap_code
				+ ", hap_staffId=" + hap_staffId + "]";
	}
	public Integer getHapId() {
		return hapId;
	}
	public void setHapId(Integer hapId) {
		this.hapId = hapId;
	}
	public String getHapName() {
		return hapName;
	}
	public void setHapName(String hapName) {
		this.hapName = hapName;
	}
	public Integer getHapType() {
		return hapType;
	}
	public void setHapType(Integer hapType) {
		this.hapType = hapType;
	}
	public String getHapCreateTime() {
		return hapCreateTime;
	}
	public void setHapCreateTime(String hapCreateTime) {
		this.hapCreateTime = hapCreateTime;
	}
	public String getHapUpdateTime() {
		return hapUpdateTime;
	}
	public void setHapUpdateTime(String hapUpdateTime) {
		this.hapUpdateTime = hapUpdateTime;
	}
	public Integer getHapSid() {
		return hapSid;
	}
	public void setHapSid(Integer hapSid) {
		this.hapSid = hapSid;
	}
	public Integer getHapUpdateSid() {
		return hapUpdateSid;
	}
	public void setHapUpdateSid(Integer hapUpdateSid) {
		this.hapUpdateSid = hapUpdateSid;
	}
	 
	public String getHap_conten() {
		return hap_conten;
	}
	public void setHap_conten(String hap_conten) {
		this.hap_conten = hap_conten;
	}
	public String getHap_remark() {
		return hap_remark;
	}
	public void setHap_remark(String hap_remark) {
		this.hap_remark = hap_remark;
	}
	public String getHap_code() {
		return hap_code;
	}
	public void setHap_code(String hap_code) {
		this.hap_code = hap_code;
	}
	public int getHap_staffId() {
		return hap_staffId;
	}
	public void setHap_staffId(int hap_staffId) {
		this.hap_staffId = hap_staffId;
	}
	
	
}
