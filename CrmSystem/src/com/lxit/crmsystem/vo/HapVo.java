package com.lxit.crmsystem.vo;

/**
 * @author Administrator
 * 机会表
 */
public class HapVo {
	private Integer hapId;  //机会id
	private String hapName;  //机会名称
	private String hapTypeValue;  //机会类型
	private String hapCreateTime;  //创建时间
	private String hapUpdateTime;  //修改时间
	private String hapSName;  //录入人
	private String hapUpdateSName;  //最后一次操作人
	
	private String hap_conten;//机会内容
	private String hap_remark;//备注信息
	private String hap_code;//机会编码
	
	@Override
	public String toString() {
		return "HapVo [hapId=" + hapId + ", hapName=" + hapName + ", hapTypeValue=" + hapTypeValue + ", hapCreateTime="
				+ hapCreateTime + ", hapUpdateTime=" + hapUpdateTime + ", hapSName=" + hapSName + ", hapUpdateSName="
				+ hapUpdateSName + ", hap_conten=" + hap_conten + ", hap_remark=" + hap_remark + ", hap_code="
				+ hap_code + "]";
	}
	public Integer getHapId() {
		return hapId;
	}
	public void setHapId(Integer hapId) {
		this.hapId = hapId;
	}
	public String getHapName() {
		return hapName;
	}
	public void setHapName(String hapName) {
		this.hapName = hapName;
	}
 
	public String getHapCreateTime() {
		return hapCreateTime;
	}
	public void setHapCreateTime(String hapCreateTime) {
		this.hapCreateTime = hapCreateTime;
	}
	public String getHapUpdateTime() {
		return hapUpdateTime;
	}
	public void setHapUpdateTime(String hapUpdateTime) {
		this.hapUpdateTime = hapUpdateTime;
	}
	public String getHapSid() {
		return hapSName;
	}
	public void setHapSid(String hapSName) {
		this.hapSName = hapSName;
	}
 
	public String getHap_conten() {
		return hap_conten;
	}
	public void setHap_conten(String hap_conten) {
		this.hap_conten = hap_conten;
	}
	public String getHap_remark() {
		return hap_remark;
	}
	public void setHap_remark(String hap_remark) {
		this.hap_remark = hap_remark;
	}
	public String getHap_code() {
		return hap_code;
	}
	public void setHap_code(String hap_code) {
		this.hap_code = hap_code;
	}
	public String getHapTypeValue() {
		return hapTypeValue;
	}
	public void setHapTypeValue(String hapTypeValue) {
		this.hapTypeValue = hapTypeValue;
	}
	public String getHapUpdateSName() {
		return hapUpdateSName;
	}
	public void setHapUpdateSName(String hapUpdateSName) {
		this.hapUpdateSName = hapUpdateSName;
	}
	
	
}
