package com.lxit.crmsystem.vo;

public class ServeVo {
	private int serveId;//服务ID
	private String serveName;//服务名称
	private String dataValue;//服务状态
	private String dataType;//服务类型
	private Integer serveType;//服务类型id
	private String serveUpdateTime;//最后一次修改的时间
	private String serveCreateTime;//创建时间
	private String serveManageName;//操作人
	private String serveStayhandle;//待处理人
	private String serveClientId;//服务客户
	private String servePhone;//联系电话
	private String serveContent;//服务内容
	private String serveRemark;//备注信息
	public int getServeId() {
		return serveId;
	}
	public void setServeId(int serveId) {
		this.serveId = serveId;
	}
	public String getServeName() {
		return serveName;
	}
	public void setServeName(String serveName) {
		this.serveName = serveName;
	}
	public String getDataValue() {
		return dataValue;
	}
	public void setDataValue(String dataValue) {
		this.dataValue = dataValue;
	}
	public String getDataType() {
		return dataType;
	}
	public void setDataType(String dataType) {
		this.dataType = dataType;
	}
	public String getServeUpdateTime() {
		return serveUpdateTime;
	}
	public void setServeUpdateTime(String serveUpdateTime) {
		this.serveUpdateTime = serveUpdateTime;
	}
	public String getServeCreateTime() {
		return serveCreateTime;
	}
	public void setServeCreateTime(String serveCreateTime) {
		this.serveCreateTime = serveCreateTime;
	}
	public String getServeManageName() {
		return serveManageName;
	}
	public void setServeManageName(String serveManageName) {
		this.serveManageName = serveManageName;
	}
	public String getServeStayhandle() {
		return serveStayhandle;
	}
	public void setServeStayhandle(String serveStayhandle) {
		this.serveStayhandle = serveStayhandle;
	}
	public String getServeClientId() {
		return serveClientId;
	}
	public void setServeClientId(String serveClientId) {
		this.serveClientId = serveClientId;
	}
	public String getServePhone() {
		return servePhone;
	}
	public void setServePhone(String servePhone) {
		this.servePhone = servePhone;
	}
	public String getServeContent() {
		return serveContent;
	}
	public void setServeContent(String serveContent) {
		this.serveContent = serveContent;
	}
	public String getServeRemark() {
		return serveRemark;
	}
	public void setServeRemark(String serveRemark) {
		this.serveRemark = serveRemark;
	}
	
	public Integer getServeType() {
		return serveType;
	}
	public void setServeType(Integer serveType) {
		this.serveType = serveType;
	}
	@Override
	public String toString() {
		return "ServeVo [serveId=" + serveId + ", serveName=" + serveName + ", dataValue=" + dataValue + ", dataType="
				+ dataType + ", serveType=" + serveType + ", serveUpdateTime=" + serveUpdateTime + ", serveCreateTime="
				+ serveCreateTime + ", serveManageName=" + serveManageName + ", serveStayhandle=" + serveStayhandle
				+ ", serveClientId=" + serveClientId + ", servePhone=" + servePhone + ", serveContent=" + serveContent
				+ ", serveRemark=" + serveRemark + "]";
	}
	
	
}
