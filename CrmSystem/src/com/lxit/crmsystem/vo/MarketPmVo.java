package com.lxit.crmsystem.vo;

public class MarketPmVo {
	
	private Integer marketPmId;//销售绩效Id
	private String marketPmCoding;//绩效编码
	private Integer staffId;//发现机会的员工Id
	private Integer marketPmLv;//绩效等级
	private String marketPmRemark;//备注
	private String marketPmCreateTime;//创建时间
	private String marketPmUpdateTime;//最后一次修改时间
	
	private String marketPmSName;//员工姓名
	private String departName;//部门
	private String operateName;//操作人
	
	public MarketPmVo(){
		
	}

	@Override
	public String toString() {
		return "MarketPmVo [marketPmId=" + marketPmId + ", marketPmCoding=" + marketPmCoding + ", staffId=" + staffId
				+ ", marketPmLv=" + marketPmLv + ", marketPmRemark=" + marketPmRemark + ", marketPmCreateTime="
				+ marketPmCreateTime + ", marketPmUpdateTime=" + marketPmUpdateTime + ", departName=" + departName
				+ ", operateName=" + operateName + "]";
	}

	public Integer getMarketPmId() {
		return marketPmId;
	}

	public void setMarketPmId(Integer marketPmId) {
		this.marketPmId = marketPmId;
	}

	public String getMarketPmCoding() {
		return marketPmCoding;
	}

	public void setMarketPmCoding(String marketPmCoding) {
		this.marketPmCoding = marketPmCoding;
	}

	public Integer getStaffId() {
		return staffId;
	}

	public void setStaffId(Integer staffId) {
		this.staffId = staffId;
	}

	public Integer getMarketPmLv() {
		return marketPmLv;
	}

	public void setMarketPmLv(Integer marketPmLv) {
		this.marketPmLv = marketPmLv;
	}

	public String getMarketPmRemark() {
		return marketPmRemark;
	}

	public void setMarketPmRemark(String marketPmRemark) {
		this.marketPmRemark = marketPmRemark;
	}

	public String getMarketPmCreateTime() {
		return marketPmCreateTime;
	}

	public void setMarketPmCreateTime(String marketPmCreateTime) {
		this.marketPmCreateTime = marketPmCreateTime;
	}

	public String getMarketPmUpdateTime() {
		return marketPmUpdateTime;
	}

	public void setMarketPmUpdateTime(String marketPmUpdateTime) {
		this.marketPmUpdateTime = marketPmUpdateTime;
	}

	public String getDepartName() {
		return departName;
	}

	public void setDepartName(String departName) {
		this.departName = departName;
	}

	public String getOperateName() {
		return operateName;
	}

	public void setOperateName(String operateName) {
		this.operateName = operateName;
	}

	public String getMarketPmSName() {
		return marketPmSName;
	}

	public void setMarketPmSName(String marketPmSName) {
		this.marketPmSName = marketPmSName;
	}
	
	
	

}
