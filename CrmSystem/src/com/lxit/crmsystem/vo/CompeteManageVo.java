package com.lxit.crmsystem.vo;
/**
 * 竞争管理表
 * @author Administrator
 *
 */
public class CompeteManageVo {
	
	private int competId;
	private String competTitle;//竞争分析结果标题
	private String competResult;//竞争结果
	private String competTypeValue;//类型
	private String competStateValue;//状态:是否归档
	private String competCreateTime;//创建时间
	private String competUpdateTime;//修改时间
	private String competSName;//操作人
	private String competUpdateSName;//最后一次修改人
	
	public CompeteManageVo(){
		
	}
	 
	@Override
	public String toString() {
		return "CompeteManageVo [competId=" + competId + ", competResult=" + competResult + ", competTypeValue="
				+ competTypeValue + ", competStateValue=" + competStateValue + ", competCreateTime=" + competCreateTime
				+ ", competUpdateTime=" + competUpdateTime + ", competSName=" + competSName + ", competUpdateSName="
				+ competUpdateSName + "]";
	}

	public String getCompetTypeValue() {
		return competTypeValue;
	}
	public void setCompetTypeValue(String competTypeValue) {
		this.competTypeValue = competTypeValue;
	}
	public String getCompetStateValue() {
		return competStateValue;
	}


	public void setCompetStateValue(String competStateValue) {
		this.competStateValue = competStateValue;
	}
	public int getCompetId() {
		return competId;
	}

	public void setCompetId(int competId) {
		this.competId = competId;
	}

	public String getCompetResult() {
		return competResult;
	}

	public void setCompetResult(String competResult) {
		this.competResult = competResult;
	}
	public String getCompetCreateTime() {
		return competCreateTime;
	}

	public void setCompetCreateTime(String competCreateTime) {
		this.competCreateTime = competCreateTime;
	}

	public String getCompetUpdateTime() {
		return competUpdateTime;
	}

	public void setCompetUpdateTime(String competUpdateTime) {
		this.competUpdateTime = competUpdateTime;
	}

	public String getCompetSName() {
		return competSName;
	}

	public void setCompetSName(String competSName) {
		this.competSName = competSName;
	}

	public String getCompetUpdateSName() {
		return competUpdateSName;
	}

	public void setCompetUpdateSName(String competUpdateSName) {
		this.competUpdateSName = competUpdateSName;
	}

	public String getCompetTitle() {
		return competTitle;
	}

	public void setCompetTitle(String competTitle) {
		this.competTitle = competTitle;
	}

 
	
	
	
	

}
